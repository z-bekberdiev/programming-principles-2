# Usage of single or double quotes
print("This will work!")
print('This will also work!')

# Output without a new line
print("Hello World!", end=" ")
print("I will print on the same line.")

# Output numbers
print(3)
print(358)
print(50000)
print(3 + 3)
print(2 * 5)

# Mixed output
print("I am", 35, "years old.")
