print(math.asin(1))                # Returns the arc sine of a number
print(math.asinh(1))               # Returns the inverse hyperbolic sine of a number
print(math.atan(1))                # Returns the arc tangent of a number in radians
print(math.atan2(1, 1))            # Returns the arc tangent of y/x in radians
print(math.atanh(0.5))             # Returns the inverse hyperbolic tangent of a number
print(math.ceil(1.7654))           # Rounds a number up to the nearest integer
print(math.comb(1, 2))             # Returns the number of ways to choose k items from n items without repetition and order
print(math.copysign(1, 2))         # Returns a float consisting of the value of the first parameter and the sign of the second parameter
