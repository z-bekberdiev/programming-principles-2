print(math.cosh(0))                # Returns the hyperbolic cosine of a number
print(math.degrees(1))             # Converts an angle from radians to degrees
print(math.dist((1, 2), (3, 4)))   # Returns the Euclidean distance between two points (p and q)), where p and q are the coordinates of that point
print(math.erf(1))                 # Returns the error function of a number
print(math.erfc(1))                # Returns the complementary error function of a number
print(math.exp(2))                 # Returns E raised to the power of x
print(math.expm1(2))               # Returns Ex - 1
print(math.fabs(-1))               # Returns the absolute value of a number
print(math.factorial(10))          # Returns the factorial of a number
print(math.floor(2.345))           # Rounds a number down to the nearest integer
print(math.fmod(4, 3))             # Returns the remainder of x/y
print(math.frexp(5))               # Returns the mantissa and the exponent, of a specified number
print(math.fsum([1, 2, 3, 4, 5]))  # Returns the sum of all items in any iterable (tuples, arrays, lists, etc.))
print(math.gamma(1))               # Returns the gamma function at x
print(math.gcd(12, 24))            # Returns the greatest common divisor of two integers
print(math.hypot())                # Returns the Euclidean norm
print(math.isclose(10, 11))        # Checks whether two values are close to each other, or not
print(math.isfinite(12))           # Checks whether a number is finite or not
print(math.isinf(-8))              # Checks whether a number is infinite or not
print(math.isnan(404))             # Checks whether a value is NaN (not a number)) or not
print(math.isqrt(2))               # Rounds a square root number downwards to the nearest integer
print(math.ldexp(5, 7))            # Returns the inverse of print(math.frexp()) which is x * (2**i)) of the given numbers x and i
print(math.lgamma(12))             # Returns the log gamma value of x
print(math.log(5, 2))              # Returns the natural logarithm of a number, or the logarithm of number to base
print(math.log10(100))             # Returns the base-10 logarithm of x
print(math.log1p(1))               # Returns the natural logarithm of 1+x
print(math.log2(12))               # Returns the base-2 logarithm of x
print(math.perm(1, 2))             # Returns the number of ways to choose k items from n items with order and without repetition
print(math.pow(10, 3))             # Returns the value of x to the power of y
print(math.prod([1, 2, 3]))        # Returns the product of all the elements in an iterable
print(math.radians(60))            # Converts a degree value into radians
print(math.remainder(5, 4))        # Returns the closest value that can make numerator completely divisible by the denominator
print(math.sin(1))                 # Returns the sine of a number
print(math.sinh(1))                # Returns the hyperbolic sine of a number
print(math.sqrt(256))              # Returns the square root of a number
print(math.tan(2))                 # Returns the tangent of a number
print(math.tanh(2))                # Returns the hyperbolic tangent of a number
print(math.trunc(1))               # Returns the truncated integer parts of a number
print(math.e)                      # Returns Euler's number (2.7182...))
print(math.inf)                    # Returns a floating-point positive infinity
print(math.nan)                    # Returns a floating-point NaN (Not a Number)) value
